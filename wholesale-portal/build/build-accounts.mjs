// build-accounts.mjs — generate the portal's vendor list from the importer's
// `customers` table, so every wholesale account that gets a portal link is the
// same customer the importer already knows (orders carry the right customer_id).
//
// Writes wholesale-portal/build/accounts.json: { accounts: { <token>: {name, slug, customer_id, email} } }
// Tokens are stable across runs (re-used by customer_id) so existing links keep working.
//
// Usage:
//   node build/build-accounts.mjs [--base https://wholesale.yakirabella.com]
//
// Env: REPORTING_DATABASE_URL (reused from ../.env if present).

import fs from "node:fs";
import path from "node:path";
import crypto from "node:crypto";
import { fileURLToPath } from "node:url";
import pg from "pg";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const OUT = path.resolve(__dirname, "accounts.json");
const ROOT = path.resolve(__dirname, "..");
const baseArg = process.argv.indexOf("--base");
const BASE_URL = baseArg !== -1 ? process.argv[baseArg + 1] : "https://wholesale.yakirabella.com";

function loadDotenvIfNeeded() {
  if (process.env.REPORTING_DATABASE_URL) return;
  for (const p of [path.resolve(ROOT, "..", ".env"), path.resolve(ROOT, ".env")]) {
    if (!fs.existsSync(p)) continue;
    for (const line of fs.readFileSync(p, "utf8").split(/\r?\n/)) {
      const m = line.match(/^\s*([A-Z0-9_]+)\s*=\s*(.*)\s*$/);
      if (m && !(m[1] in process.env)) process.env[m[1]] = m[2].replace(/^['"]|['"]$/g, "");
    }
  }
}

const slugify = (s) => String(s || "").toLowerCase().normalize("NFKD").replace(/[^\w\s-]/g, "").trim().replace(/\s+/g, "-").replace(/-+/g, "-").slice(0, 48) || "account";

async function main() {
  loadDotenvIfNeeded();
  if (!process.env.REPORTING_DATABASE_URL) { console.error("FATAL: REPORTING_DATABASE_URL not set."); process.exit(1); }

  // re-use existing tokens by customer_id so links stay stable
  let existing = { accounts: {} };
  if (fs.existsSync(OUT)) { try { existing = JSON.parse(fs.readFileSync(OUT, "utf8")); } catch {} }
  const tokenByCustomer = new Map();
  for (const [tok, a] of Object.entries(existing.accounts || {})) {
    if (a && a.customer_id != null) tokenByCustomer.set(a.customer_id, tok);
  }

  const ssl = process.env.REPORTING_DATABASE_URL.includes("sslmode=disable") ? false : { rejectUnauthorized: false };
  const pool = new pg.Pool({ connectionString: process.env.REPORTING_DATABASE_URL, ssl, max: 2 });
  const { rows } = await pool.query(
    `SELECT id, name, email FROM customers WHERE archived_at IS NULL ORDER BY LOWER(name)`
  );
  await pool.end();

  const accounts = {};
  const links = [];
  const usedSlugs = new Set();
  for (const c of rows) {
    let slug = slugify(c.name);
    let s = slug, i = 2;
    while (usedSlugs.has(s)) s = `${slug}-${i++}`;
    slug = s; usedSlugs.add(slug);

    const token = tokenByCustomer.get(c.id) || crypto.randomBytes(8).toString("hex");
    accounts[token] = { name: c.name, slug, customer_id: c.id, email: c.email || null };
    links.push({ name: c.name, link: `${BASE_URL}/?t=${token}` });
  }

  const out = {
    _comment: "Generated by build-accounts.mjs from the customers table. Tokens map a portal link to a customer (display + customer_id on orders). v1 = not security; hashed/expiring tokens come with the submit endpoint. Re-run after adding a customer; existing tokens are preserved.",
    generated_at: new Date().toISOString(),
    accounts
  };
  fs.writeFileSync(OUT, JSON.stringify(out, null, 2));

  console.log(`Wrote ${path.relative(ROOT, OUT)} — ${rows.length} vendor link(s):`);
  for (const l of links) console.log(`  ${l.name.padEnd(28)} ${l.link}`);
}

main().catch((e) => { console.error(e); process.exit(1); });
